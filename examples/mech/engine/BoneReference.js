class BoneReference {
	///////////////////////////////////////////////////////////////////////////////
	// Properites
	///////////////////////////////////////////////////////////////////////////////
	#boneIndex;
	#weight;

	///////////////////////////////////////////////////////////////////////////////
	// Constructor
	///////////////////////////////////////////////////////////////////////////////
	constructor(data) {
		if (data == null) {
			return;
		}

		this.boneIndex = data.index;
		this.weight = data.weight;

		return;
	}

	///////////////////////////////////////////////////////////////////////////////
	// Functions
	///////////////////////////////////////////////////////////////////////////////
	getBoneIndex = function() {
		return this.boneIndex;
	}
	setBoneIndex = function(index) {
		this.boneIndex = index;
		return;
	}

	getWeight = function() {
		return this.weight;
	}
	setWeight = function(weight) {
		this.weight = weight;
		return;
	}
}