class Vector3D {
	///////////////////////////////////////////////////////////////////////////////
	// Properites
	///////////////////////////////////////////////////////////////////////////////
	#data;

	///////////////////////////////////////////////////////////////////////////////
	// Constructor
	///////////////////////////////////////////////////////////////////////////////
	constructor(x, y, z) {
		this.data = [x, y, z];
		return;
	}

	///////////////////////////////////////////////////////////////////////////////
	// Functions
	///////////////////////////////////////////////////////////////////////////////
	getX = function() {
		return this.data[0];
	}
	getY = function() {
		return this.data[1];
	}
	getZ = function() {
		return this.data[2];
	}
}