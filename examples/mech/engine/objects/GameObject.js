class GameObject {
	///////////////////////////////////////////////////////////////////////////////
	// Properites
	///////////////////////////////////////////////////////////////////////////////
	#id;
	#type;

	static #idCounter = 0;

	///////////////////////////////////////////////////////////////////////////////
	// Constructor
	///////////////////////////////////////////////////////////////////////////////
	constructor(type) {

		GameObject.idCounter += 1;
		this.id = GameObject.idCounter;
		
		if (type == null) {
			this.type = "NONE";
		} else {
			this.type = type;
		}
		
		return;
	}

	///////////////////////////////////////////////////////////////////////////////
	// Functions
	///////////////////////////////////////////////////////////////////////////////
	getID = function() {
		return this.id;
	}
	setID = function(id) {
		this.id = id;
		return;
	}
}